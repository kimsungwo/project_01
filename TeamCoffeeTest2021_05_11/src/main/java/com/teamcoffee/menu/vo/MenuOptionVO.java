package com.teamcoffee.menu.vo;

public class MenuOptionVO {
	// 옵션 코드 
    private String menuOptionCode;

    // 옵션 이름 
    private String menuOptionName;

    // 옵션 가격 
    private int menuOptionPrice;

    // 옵션그룹 코드 
    private String menuOptionGroupCode;

    public String getMenuOptionCode() {
        return menuOptionCode;
    }

    public void setMenuOptionCode(String menuOptionCode) {
        this.menuOptionCode = menuOptionCode;
    }

    public String getMenuOptionName() {
        return menuOptionName;
    }

    public void setMenuOptionName(String menuOptionName) {
        this.menuOptionName = menuOptionName;
    }

    public int getMenuOptionPrice() {
        return menuOptionPrice;
    }

    public void setMenuOptionPrice(int menuOptionPrice) {
        this.menuOptionPrice = menuOptionPrice;
    }

    public String getMenuOptionGroupCode() {
        return menuOptionGroupCode;
    }

    public void setMenuOptionGroupCode(String menuOptionGroupCode) {
        this.menuOptionGroupCode = menuOptionGroupCode;
    }

    // DjvMenuOption 모델 복사
    public void CopyData(MenuOptionVO param)
    {
        this.menuOptionCode = param.getMenuOptionCode();
        this.menuOptionName = param.getMenuOptionName();
        this.menuOptionPrice = param.getMenuOptionPrice();
        this.menuOptionGroupCode = param.getMenuOptionGroupCode();
    }
}
