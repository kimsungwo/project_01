package com.teamcoffee.board.vo;

public class ImgVO {
	private String boardImgCode;
	private int boardNum;
	private String boardImgIsMain;
	private String boardUploadImgName;
	private String boardOriginImgName;

	public String getBoardImgCode() {
		return boardImgCode;
	}
	public void setBoardImgCode(String boardImgCode) {
		this.boardImgCode = boardImgCode;
	}
	public int getBoardNum() {
		return boardNum;
	}
	public void setBoardNum(int boardNum) {
		this.boardNum = boardNum;
	}
	public String getBoardImgIsMain() {
		return boardImgIsMain;
	}
	public void setBoardImgIsMain(String boardImgIsMain) {
		this.boardImgIsMain = boardImgIsMain;
	}
	public String getBoardUploadImgName() {
		return boardUploadImgName;
	}
	public void setBoardUploadImgName(String boardUploadImgName) {
		this.boardUploadImgName = boardUploadImgName;
	}
	public String getBoardOriginImgName() {
		return boardOriginImgName;
	}
	public void setBoardOriginImgName(String boardOriginImgName) {
		this.boardOriginImgName = boardOriginImgName;
	}
	
}