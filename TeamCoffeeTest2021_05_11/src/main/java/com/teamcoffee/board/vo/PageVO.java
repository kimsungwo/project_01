package com.teamcoffee.board.vo;

public class PageVO {
	private int nowPage;		// 현재 선택 페이지
	private int totalCnt;		// 게시글 전체 수
	private int beginPage;		// 화면상 첫 페이지
	private int endPage;		// 화면상 마지막 페이지
	private int displayCnt; 	// 페이지당 게시글 수
	private int displayPageCnt;	// 화면상 페이지 수
	
	public int getNowPage() {
		return nowPage;
	}
	public void setNowPage(int nowPage) {
		this.nowPage = nowPage;
	}
	public int getTotalCnt() {
		return totalCnt;
	}
	public void setTotalCnt(int totalCnt) {
		this.totalCnt = totalCnt;
	}
	public int getBeginPage() {
		return beginPage;
	}
	public void setBeginPage(int beginPage) {
		this.beginPage = beginPage;
	}
	public int getEndPage() {
		return endPage;
	}
	public void setEndPage(int endPage) {
		this.endPage = endPage;
	}
	public int getDisplayCnt() {
		return displayCnt;
	}
	public void setDisplayCnt(int displayCnt) {
		this.displayCnt = displayCnt;
	}
	public int getDisplayPageCnt() {
		return displayPageCnt;
	}
	public void setDisplayPageCnt(int displayPageCnt) {
		this.displayPageCnt = displayPageCnt;
	}
	
	
}